



package calculadora;


public class Calculadora {

    double numero1;
    double numero2;
    
    public double Sumar(){
        
        return(numero1+numero2);
        
    }
    
    public double Multiplicar(){
        
        return(numero1*numero2);
        
    }

    public double Dividir(){
        
        return(numero1/numero2);
        
    }
    
    public double Restar(){
        
        return(numero1-numero2);
        
    }
    
    
}
